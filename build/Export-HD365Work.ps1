    # Safety net: fail loudly instead of shipping a broken script if the exact text above drifted.
    $errs = $null
    [void][System.Management.Automation.Language.Parser]::ParseFile($installPath, [ref]$null, [ref]$errs)
    if ($errs -and $errs.Count -gt 0) {
        throw "Export-HD365Work: Install-HD365.ps1 failed to parse after trim ($($errs[0].Message)). Update the exact-text replacements in this script to match the current source."
    }
}

# --- Soften Show-HD365AiSetupHelp provider section ---
$copilotPath = Join-Path $stage 'Private\Invoke-HD365Copilot.ps1'
if (Test-Path -LiteralPath $copilotPath) {
    $c = Get-Content -LiteralPath $copilotPath -Raw -Encoding UTF8
    $c = $c -replace "(?s)Write-Host 'Other providers \(agnostic line\):'.*?Write-Host ''", @"
