$settings = Get-Content -LiteralPath $settingsPath -Raw -Encoding UTF8 | ConvertFrom-Json
$settings.ai.provider = 'CopilotChat'
$settings.ai.allowOfflineFallback = $false
if ($settings.ai.providers) {
    foreach ($prop in @($settings.ai.providers.PSObject.Properties.Name)) {
        if ($prop -notin @('AzureOpenAI')) {
            $settings.ai.providers.PSObject.Properties.Remove($prop)
        }
    }
}
($settings | ConvertTo-Json -Depth 8) | Set-Content -LiteralPath $settingsPath -Encoding UTF8

