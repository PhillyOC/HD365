#Requires -Version 5.1
<#
.SYNOPSIS
  Export a CopilotChat + AzureOpenAI-only HD365 tree as a zip for carry to work Copilot Git.
  Replaces Private\Invoke-HD365Providers.ps1 with a trimmed 2-provider catalog so the
  Pro-only providers (OpenAI, Anthropic, Gemini, Together, Mistral, Ollama) are never
  present in the work build - not just hidden from the menu.
#>
[CmdletBinding()]
param(
    [string]$RepoRoot,
    [string]$OutDir
)

$ErrorActionPreference = 'Stop'

if (-not $RepoRoot) {
    if ($PSScriptRoot) { $RepoRoot = Split-Path $PSScriptRoot -Parent }
    else { $RepoRoot = 'E:\MS365\HD365' }
}

$manifest = Import-PowerShellDataFile -Path (Join-Path $RepoRoot 'HD365.psd1')
$version = [string]$manifest.ModuleVersion
$stamp = Get-Date -Format 'yyyyMMdd'

if (-not $OutDir) { $OutDir = Join-Path $RepoRoot 'dist' }
if (-not (Test-Path -LiteralPath $OutDir)) {
    New-Item -ItemType Directory -Path $OutDir -Force | Out-Null
}

$stageName = "HD365-work-$version-$stamp"
$stage = Join-Path $OutDir $stageName
if (Test-Path -LiteralPath $stage) { Remove-Item -LiteralPath $stage -Recurse -Force }
New-Item -ItemType Directory -Path $stage -Force | Out-Null

$include = @(
    'HD365.psd1', 'HD365.psm1', 'Start-HD365.ps1', 'Install-HD365.ps1',
    'README.md', 'CHANGELOG.md', 'VERSIONING.md', 'SYNC.md', 'LICENSE',
    'Public', 'Private', 'Config', 'Tests'
)
foreach ($item in $include) {
    $src = Join-Path $RepoRoot $item
    if (-not (Test-Path -LiteralPath $src)) { continue }
    Copy-Item -LiteralPath $src -Destination (Join-Path $stage $item) -Recurse -Force
}

# --- Provider firewall: replace the catalog module with CopilotChat + AzureOpenAI only ---
$providersPath = Join-Path $stage 'Private\Invoke-HD365Providers.ps1'
$workProviders = @'
# WORK EXPORT: CopilotChat + AzureOpenAI only. Generated by Export-HD365Work.ps1.
# Pro-only providers (OpenAI, Anthropic, Gemini, Together, Mistral, Ollama) are
# intentionally excluded from this build - not just hidden from the /ai menu.

function Get-HD365ProviderCatalog {
    [CmdletBinding()]
    param()

    @(
        [pscustomobject]@{
            Id             = 'CopilotChat'
            DisplayName    = 'Microsoft 365 Copilot Chat'
            Kind           = 'Copilot'
            NeedsKey       = $false
            NeedsEndpoint  = $false
            DefaultModel   = $null
            DefaultBaseUrl = 'https://graph.microsoft.com/beta'
            KeyEnvVar      = $null
            Notes          = 'Requires Microsoft 365 Copilot add-on license (work/school account).'
        }
        [pscustomobject]@{
            Id             = 'AzureOpenAI'
            DisplayName    = 'Azure OpenAI'
            Kind           = 'AzureOpenAI'
            NeedsKey       = $true
            NeedsEndpoint  = $true
            DefaultModel   = 'gpt-4o'
            DefaultBaseUrl = $null
            KeyEnvVar      = 'HD365_AZURE_OPENAI_KEY'
            Notes          = 'Set endpoint + deployment in settings.json ai.providers.AzureOpenAI.'
        }
    )
}

function Get-HD365ProviderSettings {
    [CmdletBinding()]
    param(
        [Parameter(Mandatory)]
        [string]$Id
    )

    $entry = @(Get-HD365ProviderCatalog) | Where-Object { $_.Id -eq $Id } | Select-Object -First 1
    if (-not $entry) {
        throw "Unknown or unsupported AI provider '$Id' in this build. This build is CopilotChat + AzureOpenAI only."
    }

    $userCfg = $null
    if ($script:HD365Config -and $script:HD365Config.ai -and $script:HD365Config.ai.providers -and
        $script:HD365Config.ai.providers.PSObject.Properties[$Id]) {
        $userCfg = $script:HD365Config.ai.providers.$Id
    }

    $model = $entry.DefaultModel
    if ($userCfg -and $userCfg.PSObject.Properties['model'] -and $userCfg.model) { $model = [string]$userCfg.model }

    $endpoint = $null
    if ($userCfg -and $userCfg.PSObject.Properties['endpoint'] -and $userCfg.endpoint) { $endpoint = [string]$userCfg.endpoint.TrimEnd('/') }

    $deployment = $null
    if ($userCfg -and $userCfg.PSObject.Properties['deployment'] -and $userCfg.deployment) { $deployment = [string]$userCfg.deployment }

    $apiVersion = '2024-10-21'
    if ($userCfg -and $userCfg.PSObject.Properties['apiVersion'] -and $userCfg.apiVersion) { $apiVersion = [string]$userCfg.apiVersion }

    $keyEnvVar = $entry.KeyEnvVar
    if ($userCfg -and $userCfg.PSObject.Properties['apiKeyEnvVar'] -and $userCfg.apiKeyEnvVar) { $keyEnvVar = [string]$userCfg.apiKeyEnvVar }

    [pscustomobject]@{
        Id            = $entry.Id
        DisplayName   = $entry.DisplayName
        Kind          = $entry.Kind
        NeedsKey      = $entry.NeedsKey
        NeedsEndpoint = $entry.NeedsEndpoint
        Notes         = $entry.Notes
        Model         = $model
        BaseUrl       = $null
        Endpoint      = $endpoint
        Deployment    = $deployment
        ApiVersion    = $apiVersion
        KeyEnvVar     = $keyEnvVar
    }
}

function Get-HD365ProviderApiKey {
    [CmdletBinding()]
    param(
        [Parameter(Mandatory)]
        [object]$Settings
    )

    $envVar = $Settings.KeyEnvVar
    if (-not $envVar) {
        throw "$($Settings.DisplayName) has no configured API key environment variable."
    }

    $key = [Environment]::GetEnvironmentVariable($envVar, 'Process')
    if (-not $key) { $key = [Environment]::GetEnvironmentVariable($envVar, 'User') }
    if (-not $key) { $key = [Environment]::GetEnvironmentVariable($envVar, 'Machine') }

    if (-not $key) {
        throw "$($Settings.DisplayName) API key not found. Set environment variable '$envVar'."
    }
    return $key
}

function Test-HD365ProviderConfigured {
    [CmdletBinding()]
    param(
        [Parameter(Mandatory)]
        [string]$Id
    )

    try { $settings = Get-HD365ProviderSettings -Id $Id }
    catch { return $false }

    switch ($settings.Kind) {
        'Copilot' { return $true }
        'AzureOpenAI' {
            if (-not $settings.Endpoint -or -not $settings.Deployment) { return $false }
            try { $null = Get-HD365ProviderApiKey -Settings $settings; return $true }
            catch { return $false }
        }
        default { return $false }
    }
}

function Invoke-HD365ProviderChat {
    [CmdletBinding()]
    param(
        [Parameter(Mandatory)]
        [string]$ProviderId,

        [Parameter(Mandatory)]
        [string]$System,

        [Parameter(Mandatory)]
        [string]$User,

        [double]$Temperature = 0.2,

        [int]$MaxTokens = 8192
    )

    $settings = Get-HD365ProviderSettings -Id $ProviderId

    switch ($settings.Kind) {
        'Copilot' {
            return Invoke-HD365CopilotChat -Message $User -SystemPreamble $System
        }

        'AzureOpenAI' {
            if (-not $settings.Endpoint -or -not $settings.Deployment) {
                throw "Azure OpenAI endpoint and deployment must be set in settings.json (ai.providers.AzureOpenAI)."
            }
            $apiKey = Get-HD365ProviderApiKey -Settings $settings
            $uri = "$($settings.Endpoint)/openai/deployments/$($settings.Deployment)/chat/completions?api-version=$($settings.ApiVersion)"
            $body = @{
                messages        = @(
                    @{ role = 'system'; content = $System }
                    @{ role = 'user'; content = $User }
                )
                temperature     = $Temperature
                max_tokens      = $MaxTokens
                response_format = @{ type = 'json_object' }
            } | ConvertTo-Json -Depth 10
            $headers = @{ 'api-key' = $apiKey; 'Content-Type' = 'application/json' }
            $resp = Invoke-RestMethod -Method Post -Uri $uri -Headers $headers -Body $body -TimeoutSec 180
            return [string]$resp.choices[0].message.content
        }

        default {
            throw "This HD365 build is CopilotChat + AzureOpenAI only. Unsupported provider '$ProviderId'."
        }
    }
}
'@
Set-Content -LiteralPath $providersPath -Value $workProviders -Encoding UTF8

# --- Settings example trim: drop Pro-only provider blocks, keep AzureOpenAI ---
